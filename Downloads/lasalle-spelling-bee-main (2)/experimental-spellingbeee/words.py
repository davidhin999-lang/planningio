# Fixed to exactly 4 weeks for all groups
NUM_WEEKS = 4

def _chunk_into_weeks(words, num_weeks=4):
    """Divide words into exactly num_weeks chunks, distributing evenly."""
    if not words:
        return [[] for _ in range(num_weeks)]
    chunk_size = len(words) // num_weeks
    remainder = len(words) % num_weeks
    weeks = []
    start = 0
    for i in range(num_weeks):
        end = start + chunk_size + (1 if i < remainder else 0)
        weeks.append(words[start:end])
        start = end
    return weeks

WORDS_PER_WEEK = 30  # kept for backward compat but not used for chunking

def _chunk(words, chunk_size):
    """Divide words into weekly chunks (backward compat)."""
    return [words[i:i+chunk_size] for i in range(0, len(words), chunk_size)]

def _chunk_balanced(easy, medium, hard, min_per_difficulty=10):
    """Divide words into weeks ensuring minimum words per difficulty."""
    max_weeks = min(
        len(easy) // min_per_difficulty if easy else 0,
        len(medium) // min_per_difficulty if medium else 0,
        len(hard) // min_per_difficulty if hard else 0
    )
    if max_weeks == 0:
        max_weeks = 1
    weeks = []
    for week_num in range(max_weeks):
        easy_start = week_num * (len(easy) // max_weeks)
        easy_end = (week_num + 1) * (len(easy) // max_weeks)
        if week_num == max_weeks - 1:
            easy_end = len(easy)
        medium_start = week_num * (len(medium) // max_weeks)
        medium_end = (week_num + 1) * (len(medium) // max_weeks)
        if week_num == max_weeks - 1:
            medium_end = len(medium)
        hard_start = week_num * (len(hard) // max_weeks)
        hard_end = (week_num + 1) * (len(hard) // max_weeks)
        if week_num == max_weeks - 1:
            hard_end = len(hard)
        weeks.append({
            "week": week_num + 1,
            "easy": easy[easy_start:easy_end],
            "medium": medium[medium_start:medium_end],
            "hard": hard[hard_start:hard_end],
        })
    return weeks, max_weeks

