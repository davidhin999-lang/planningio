def get_words_with_weeks(group="group1"):
    """Return all words with their week numbers for review sorting."""
    result = {"easy": [], "medium": [], "hard": []}
    for difficulty in ["easy", "medium", "hard"]:
        weeks = get_group_weeks(group, difficulty)
        for week_num, week_words in enumerate(weeks, 1):
            for word in week_words:
                result[difficulty].append({"word": word, "week": week_num})
    return result


